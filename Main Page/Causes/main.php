  		<h3>Sewage</h3>
  		<img src="img/sewage.jpg" style="width: 300px; height: 200px">
  		<p>With billions of people on the planet, disposing of sewage waste is a major problem. Sewage disposal affects people's immediate environments and leads to water-related illnesses such as diarrhea that kills 525,000 children under five each year. Yet the problem of sewage disposal does not end there. When you flush the toilet, the waste has to go somewhere and, even after it leaves the sewage treatment works, there is still waste to dispose of. In practice, sewage contains all kinds of other chemicals, from the pharmaceutical drugs people take to the paper, plastic, and other wastes they flush down their toilets. When people are sick with viruses, the sewage they produce carries those viruses into the environment. It is possible to catch illnesses such as hepatitis, typhoid, and cholera from rivers.</p>
		</div>

		<div id="Nutrients" class="tabcontent">
  		<h3>Nutrients</h3>
  		<img src="img/nutrients.jpg" style="width: 300px; height: 200px">
  		<p>Chemical fertilizers used by farmers also add nutrients to the soil, which drain into rivers and seas and add to the fertilizing effect of the sewage. Together, sewage and fertilizers can cause a massive increase in the growth of algae or plankton that overwhelms huge areas of oceans, lakes, or rivers. This is known as a harmful algal bloom. It is harmful because it removes oxygen from the water that kills other forms of life, leading to what is known as a dead zone. </p>
		</div>

		<div id="WasteWater" class="tabcontent">
 		<h3>Waste Water</h3>
 		<img src="img/waste_water.png" style="width: 300px; height: 200px">
  		<p>Factories are point sources of water pollution, but quite a lot of water is polluted by ordinary people from nonpoint sources; this is how ordinary water becomes waste water in the first place. Virtually everyone pours chemicals of one sort or another down their drains or toilets. Even detergents used in washing machines and dishwashers eventually end up in our rivers and oceans. So do the pesticides we use on our gardens. A lot of toxic pollution also enters waste water from highway runoff.</p>
		</div>

		<div id="ChemicalWaste" class="tabcontent">
 		<h3>Chemical Waste</h3>
 		<img src="img/chemical_waste.jpg" style="width: 300px; height: 200px">
  		<p>Another kind of toxic pollution comes from heavy metals, such as lead, cadmium, and mercury. Lead was once commonly used in gasoline (petrol), though its use is now restricted in some countries. Mercury and cadmium are still used in batteries (though some brands now use other metals instead). </p>
		</div>

		<div id="RadioactiveWaste" class="tabcontent">
 		<h3>Radioactive Waste</h3>
 		<img src="img/radioactive_waste.jpg" style="width: 300px; height: 200px">
  		<p>Radioactive waste is any pollution that emits radiation beyond what is naturally released by the environment. It’s generated by uranium mining, nuclear power plants, and the production and testing of military weapons, as well as by universities and hospitals that use radioactive materials for research and medicine. Radioactive waste can persist in the environment for thousands of years, making disposal a major challenge </p>
		</div>

		<div id="Plastics" class="tabcontent">
 		<h3>Plastics</h3>
 		<img src="img/plastics.jpg" style="width: 300px; height: 200px">
  		<p>Plastic is one of the most common materials, used for making virtually every kind of manufactured object from clothing to automobile parts; plastic is light and floats easily so it can travel enormous distances. Most plastics are not biodegradable (they do not break down naturally in the environment), which means that things like plastic bottle tops can survive in the marine environment for a long time. While plastics are not toxic in quite the same way as poisonous chemicals, they nevertheless present a major hazard to seabirds, fish, and other marine creatures. For example, plastic fishing lines and other debris can strangle or choke fish. </p>
		</div>

		<div id="AlienSpecies" class="tabcontent">
 		<h3>Alien Species</h3>
 		<img src="img/alien_species.jpg" style="width: 200px; height: 300px">
  		<p>Alien species (sometimes known as invasive species) are animals or plants from one region that have been introduced into a different ecosystem where they do not belong. Outside their normal environment, they have no natural predators, so they rapidly run wild, crowding out the usual animals or plants that thrive there. </p>
		</div>

		<div id="TermalPollution" class="tabcontent">
