<button class="accordion">Poor Urban Planning</button>
<div class="panel">
<br>
<img src="img/poor_urban.png" style="width: 500px; height: 300px">
<br>
  	<p>In most of the developing countries, poor urban planning also plays a vital role. Congested houses, large families sharing small space, fight over parking, frequent fights over basic amenities lead to noise pollution, which may disrupt the environment of society.</p>
	<p>Noise pollution in urban settings may also be caused when residential properties and industrial buildings are in proximity. In situations like these, the noise from the nearby industrial property might hinder the basic well-being of the individuals living in residential properties.</p>
	<p>It doesn’t just affect their sleep and hours of rest but also has an adverse effect on the development and well-being of children.</p>
