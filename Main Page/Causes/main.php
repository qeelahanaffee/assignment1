  	<p>Noise is at its peak in most of the social events. Whether it is marriage, parties, pub, disc or place of worship, people normally flout rules set by the local administration and create a nuisance in the area.</p>
	<p>People play songs on full volume and dance till midnight, which makes the condition of people living nearby pretty worse. In markets, you can see people selling clothes via making a loud noise to attract the attention of people.</p>
	<p>While this may not seem like much at the outset, over time, it affects the hearing abilities of the individuals who are constantly exposed to these sounds.
</p>
