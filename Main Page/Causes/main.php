			<p>Light pollution, or excessive artificial light, is particularly prominent at night. It is mainly caused by misdirected, excessive, inefficient and unnecessary lighting systems. In urban areas where artificial light sources (e.g. street lights) are abundant, the phenomenon is common. When light shines upward, downward, or is reflected upward, it is dispersed by layers in the atmosphere, which results in a glow that reduces the darkness of night sky. </p>
